#Operators 
#1.Arithmetic Operators
a = 5
b = 3
print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a**b)
print(a%b)

#2.Relational Operator
a = 20
b = 70
print(a==b)
print(a!=b)
print(a>=b)
print(a<=b)
print(a>b)
print(a<b)

#3.Assignment Operator
num = 10
num += 10
num -= 10
num *= 10
num /= 10
num **= 10
num %= 10
print(num)

#3.Logical operator

a = 60
b = 20
print(not False)
print(not True)
print((a==b and a>b))

